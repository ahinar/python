# Escribe un módulo que contenga varias funciones matemáticas (suma, resta, multiplicación, división).
# Luego, importa este módulo en un nuevo script y utiliza las funciones para realizar cálculos.
def suma (a,b):
    return a+b
   

def resta (a,b):
    return a-b

def producto (a,b):
    return a*b

def division (a,b):
    return a/b
